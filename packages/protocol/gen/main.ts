import { dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

import { PacketIds } from '../src/enums';
import { file } from './code-gen-builder';

// Reexports
const REEXPORTS = new URL('../src/packets/index.ts', import.meta.url);
{
   const imports = await Array.fromAsync(
      new Bun.Glob('*.ts').scan({ absolute: false, cwd: dirname(fileURLToPath(REEXPORTS)), onlyFiles: true })
   );
   imports.toSorted();
   // Code
   await using _code = file(REEXPORTS)
      .ln(`///////////////////////////////////////////////`)
      .ln(`// This file is generated do not edit this file`)
      .ln(`///////////////////////////////////////////////`)
      .ln()
      .each(imports, (_, i) =>
         i.includes('index') ? _ : _.ln(`export * from './${i.substring(0, i.length - 3)}';`)
      );
}

const ALL_PACKETS = await import(REEXPORTS.href);
await using _types = file(new URL('../src/proto.ts', import.meta.url))
   .ln(`///////////////////////////////////////////////`)
   .ln(`// This file is generated do not edit this file`)
   .ln(`///////////////////////////////////////////////`)
   .ln()
   .ln(`import { PacketIds } from './enums';`)
   .ln()
   .ln(`import { `)
   .map(ALL_PACKETS, (_, k, v) => ('packetId' in v ? _.ln(`   ${v.name}, `) : _))
   .ln(`} from './packets';`)
   .ln()
   .ln(`export const PROTOCOL = {`)
   .map(ALL_PACKETS, (_, k, v) =>
      'packetId' in v ? _.ln(`   [PacketIds.${PacketIds[v.packetId]}]: ${v.name},`) : _
   )
   .ln('} as const;')
   .ln()
   .ln(`export interface InternalPacketPipelineMethods<T, R = void>  {`)
   .map(ALL_PACKETS, (_, k, v) =>
      'packetId' in v ? _.ln(`   [PacketIds.${PacketIds[v.packetId]}]?(source: T, packet: ${v.name}): R;`) : _
   )
   .ln('}');
