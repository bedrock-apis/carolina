import { PacketIds } from '../src/enums';
import { CodecPacketsSerializers, CodecTypesSerializers } from '../src/protocol';
import { file } from './code-gen-builder';

// Code
await using _code = file(new URL('../src/gen/protocol-types.js', import.meta.url))
   .ln(`// This file is generated do not edit this file`)
   .ln(`import { codec } from '../protocol';`)
   .ln()
   .ln(`export const {`)
   .map(CodecPacketsSerializers, (_, packetIds, packetType) => `   "${packetIds}": ${packetType.name},`)
   .ln(`} = codec.packets;`)
   .ln()
   .ln(`export const {`)
   .map(CodecTypesSerializers, (_, name, __) => `   ${name},`)
   .ln(`} = codec.types;`);

// Types
await using _types = file(new URL('../src/gen/protocol-types.d.ts', import.meta.url))
   // Heading
   .ln(`// This file is generated do not edit this file`)
   .ln(`import type { PacketIds } from '../enums';`)
   .ln(`import type { PacketType } from '../packet';`)
   .ln()
   .ln(
      `import { CodecPacketsSerializers, CodecTypesSerializers, CodecPackets, CodecTypes } from '../protocol';`
   )
   .ln()
   // Exports
   .map(CodecPacketsSerializers, (_, packetIds, packetType) =>
      _.ln(`export const ${packetType.name}: CodecPacketsSerializers['${packetIds}'];`).ln(
         `export type ${packetType.name} = CodecPackets['${packetIds}'];`
      )
   )
   .ln()
   .map(CodecTypesSerializers, (_, packetIds) =>
      _.ln(`export const ${packetIds}: CodecTypesSerializers['${packetIds}'];`).ln(
         `export type ${packetIds} = CodecTypes['${packetIds}'];`
      )
   )
   .ln()
   // Interface
   .ln(`export interface InternalPacketPipelineMethods<T, R = void> {`)
   .map(
      CodecPacketsSerializers,
      (_, packetIds, packetType) =>
         `   [PacketIds.${PacketIds[packetIds]}]?(source: T, packet: ${packetType.name}): R;`
   )
   .ln(`};`);
